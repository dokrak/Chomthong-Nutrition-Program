            <li>ชื่อแพทย์ :จำเป็นต้องใส่เพื่อแสดงรายชื่อแพทย์เจ้าของไข้</li>
            <li>user : การกำหนดสิทธิของผู้ใช้ให้ได้ตั้งแต่ level 1-3 โดย 
              <ul>
                <li>level 1 สามารถบันทึกข้อมูลได้อย่างเดียว</li>
                <li>level 2 สามารถเพิ่ม-แก้ไขรายชื่อ user รายชื่อแพทย์ และรายชื่อหน่วยงานได้ แต่กำหนดสิทธิ์ให้ user ได้แค่ level 2</li>
                <li>level 3 สามารถแก้ไขข้อมูลได้ทุกอย่างและสามารถกำหนดสิทธิ์ให้ผู้ใช้ได้ถึง level 3</li>
              </ul>
            </li>
            <li> หน่วยงาน : ใส่รายชื่อหน่วยงานที่เกี่ยวข้องใน รพ.</li>
