            <li>รายชื่อ รพ. ในเครือข่าย : กรณีที่ใช้โปรแกรมร่วมกันในเครือข่าย และมีหลายโรงพยาบาลใช้ร่วมกัน การบันทึกข้อมูลของแต่ละโณงพยาบาลจะแยกส่วนกัน</li>
          </ul>
        </li>
      </ul>
